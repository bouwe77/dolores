namespace Dolores.Http
{
   /// <summary>
   /// Enumeration for the HTTP methods that are supported by Dolores.
   /// </summary>
   public enum HttpMethod
   {
      /// <summary>
      /// The HTTP GET method.
      /// </summary>
      Get,
      /// <summary>
      /// The HTTP POST method.
      /// </summary>
      Post,
      /// <summary>
      /// The HTTP PUT method.
      /// </summary>
      Put,
      /// <summary>
      /// The HTTP DELETE method.
      /// </summary>
      Delete,
      /// <summary>
      /// The HTTP PATCH method.
      /// </summary>
      Patch,
      /// <summary>
      /// The HTTP HEAD method.
      /// </summary>
      Head,
      /// <summary>
      /// The HTTP OPTIONS method.
      /// </summary>
      Options
   }
}
